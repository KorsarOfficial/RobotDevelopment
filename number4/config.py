#!/usr/bin/env python
# -*- coding: utf-8 -*-

"""
Файл конфигурации для примера вращения робота RM 001 вокруг оси X
"""

# Режим эмуляции (True - для симуляции, False - для реального робота)
EMULATION_MODE = True

# Параметры движения робота
SERVO_SPEED = 150  # Угловая скорость сервоприводов (0-400 градусов/сек для mg90s)
TIMEOUT_SETUP_PULSE = 0.5  # Таймаут после установки импульса сервопривода

# Параметры сервоприводов
SERVO_BASE_PORT = 0  # Порт для сервопривода основания (Z ось)
SERVO_SHOULDER_PORT = 1  # Порт для сервопривода плеча (X ось)
SERVO_ELBOW_PORT = 2  # Порт для сервопривода локтя
SERVO_WRIST_PORT = 3  # Порт для сервопривода запястья
SERVO_GRIPPER_PORT = 4  # Порт для сервопривода захвата

# I2C адаптер (для CP2112)
I2C_ADAPTER_TYPE = "cp2112"

# ШИМ модулятор
PWM_TYPE = "pca9685"
PWM_ADDRESS = 0x40  # Адрес ШИМ модулятора на шине I2C

# Параметры вращения по оси X
X_ROTATION_PARAMS = {
    "min_angle": -45,  # минимальный угол поворота (градусы)
    "max_angle": 45,   # максимальный угол поворота (градусы)
    "steps": 5,        # количество шагов между минимальным и максимальным углом
    "pause": 0.5       # пауза между поворотами (секунды)
}

# Константы для управления программой через пользовательский интерфейс
USER_CHOICE_START = "1"  # Пользователь подтверждает начало выполнения
USER_CHOICE_EXIT = "2"   # Пользователь подтверждает выход из программы

# Параметры логирования
LOG_LEVEL = "INFO"  # уровень логирования (DEBUG, INFO, WARNING, ERROR, CRITICAL)
LOG_FILE = "robot_rotation.log"  # файл для записи логов 